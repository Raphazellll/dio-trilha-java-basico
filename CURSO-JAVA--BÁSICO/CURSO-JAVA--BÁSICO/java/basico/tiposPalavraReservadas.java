public class tiposPalavraReservadas {

    /*
     Assuntos: 
        * Tipos primitivos
        * Classificações
        * Escopo de uso
        * Palavras "opostas"
     

    Palavras reservadas são identificadores de uma linguaguem que já possuem uma finalidade específica,
        pórtanto não podem ser utilizazdos para nomear variáveis, classes, métodos ou atributos.

    A linguaguem JAVA possui 52 palavras reservadas. Todas essas palavras são classificadas em grupos e
        escritas com letras minúscula, sendo idedntificada com uma cor especial pela maioria da IDE'S.
        Abaixo temos a lista de palavras agrupadas por suas finalidades.

        
        * Controle de pacotes:
            import: importa pacotes ou classes para dentro do código.

            package: especifica a que pacote todas as classes de um arquivo pertencem
        
        * Modificadores de acesso:
            public: acesso de qualquer classe

            private: acesso apenas dentro da classe

            protected: acesso por classes no mesmo pacote e sub-classes
        
        * Primitivos:
            boolean: um valor indicando verdadeiro ou falso 

            byte: um inteiro de 8 bits (signed)

            char: um caractere unicode (16-bit unsigned)

            double: um número de ponto flutuante de 64 bits (signed)

            float: um número de ponto flutuante de 32 bits (signed)

            int: um valor inteiro de 32 bits (signed)

            long: um valor inteiro de 64 bits (signed)

            short: um valor inteiro de 32 bits (signed)

            void: indica que o método não tem retorno de valor

        * Modificadores de classes, variáveis ou métodos:
            abstract: classe que não pode ser instanciada ou método que precisa ser implementado
            por uma sub-classe não abstrata
            class: especifica uma classe

            extends: indica a superclasse que a sub-classe está estendendo
            final: impossibilita que uma classe seja estendida, que um método seja sobrescrito
            ou que uma variável seja reinicializada

            implements: indica as interfaces que uma classe irá implementar
            interface: especifica uma interface

            native: infica que um método está escrito em uma linguaguem dependente de plataforma, como
            o C 

            new: instancia um novo objeto, chamando seu constutor
            static: faz o método ou variável pertencer à classe ao invés de às instâncias

            strctfp: usado em frente a um método ou classe para indicar que os números de ponto
            flutuante seguirão as regras de ponto flutuante em todas as expressões

            synchronized: indica que um método só pode ser acessado por uam thread de cada vez

            transient: impede a serialização de campos (impede de salvar informações)

            volatile: indica que uma variável pode ser alterada durante o uso de threads
        
        
        * Controle de fluxo dentro de um bloco de código:
            break: sai do bloco de código em que ele está

            case: executa um bloco de código dependendo do teste do switch

            continue: pula a execução do código que viria após essa linha e vai para a próxima
            passagem do loop


    ======================================================================================================

        Palavras "opostas"
            Assim como nas classificações gramaticais da língua portuguesa, existem algumas
            palavras que são completamente opostas (antônimas) na linguagem JAVA.

     */
    
}
